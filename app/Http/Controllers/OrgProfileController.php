    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $orgProfile = new NgoProfile();

        $this->validate($request, [
            'org_Name'=>'required',
            'moto'=>'required',
            'file'=>'image|mimes:jpg,png,jpeg'
        ]);

        // File name With Extension
        $fileNameWithExt = $request->file('file')->getClientOriginalName();

        // Just File Name
        $filename = pathinfo($fileNameWithExt, PATHINFO_FILENAME);

        // Get With Extension
        $extension = $request->file('file')->getClientOriginalExtension();

        // Create a New File
        $fileNameToStore = $filename.'_'.time().'_'.$extension;

        // Upload Image
        $path = $request->file('file')->storeAs('public/ngo_images',$fileNameToStore);

        $orgProfile->file=$fileNameToStore;
        $orgProfile->org_Name=$request->org_Name;
        $orgProfile->moto=$request->moto;
        $orgProfile->about=$request->about;
        
        
        $orgProfile->save();

        return redirect()->back()->with('message','Profile Updated');
    }

